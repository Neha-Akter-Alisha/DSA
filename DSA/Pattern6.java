//Print the pattern of a Half Pyramid of Numbers.
public class Pattern6 {
    public static void main(String[] args) {
        int n =5;

        //outer loop
        for(int i=1; i<=n; i++) {
            for(int j =1; j<=i; j++) {
                System.out.print(j+" ");
            }
   System.out.println();
  }
 }
}
